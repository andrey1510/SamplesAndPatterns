public class IfElse {

    public static void main(String[] args) {
    int i = 10;

    // IfElse sample
    if (i < 1) {
        System.out.println(i);
    } else if (i < 3) {
        System.out.println(i);
    } else if (i < 5) {
        System.out.println(i);
    } else {
        System.out.println(i);
    }
}

}
